package bfp.stackoverflowclient.questions;

public class ProgrammingLanguageTag {


    String name;
    String tag;

    public ProgrammingLanguageTag(String name,String tag) {
        this.name = name;
        this.tag = tag;
    }




    public String getTag() {
        return tag;
    }
    public String getName() {
        return name;
    }

}
